class GraphNet(nn.Module):
    def __init__(self, encoder, decoder, k, patch_size, stride, conv_type):
        super(GraphNet, self).__init__()
        enc_layers = list(encoder.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.dec_2 = decoder_block2
        self.dec_3 = decoder_block3
        self.graph = GraphBlock(n_c=256, dist='normcrosscorrelation', conv=conv_type, k=k, patch_size=patch_size, stride=stride)
        self.mse_loss = nn.MSELoss()
        
        self.update_layers = nn.Sequential(self.graph, self.dec_2, self.dec_3)

        # fix the encoder
        for name in ['enc_1', 'enc_2', 'enc_3']:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu1_1, relu2_1, relu3_1 from input image
    def encode_with_intermediate(self, input):
        results = [input]
        for i in range(3):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        return results[1:]

    # extract relu3_1 from input image
    def encode(self, input):
        for i in range(3):
            input = getattr(self, 'enc_{:d}'.format(i + 1))(input)
        return input

    def calc_content_loss(self, input, target):
        #assert (input.size() == target.size())
        return self.mse_loss(input, target)

    def calc_style_loss(self, input, target):
        #assert (input.size() == target.size())
        #assert (target.requires_grad is False)
        input_mean, input_std = calc_mean_std(input)
        target_mean, target_std = calc_mean_std(target)
        return self.mse_loss(input_mean, target_mean) + \
               self.mse_loss(input_std, target_std)

    def forward(self, content, style, alpha=1.0,is_training=False):
        # encoder
        assert 0 <= alpha <= 1
        style_feats = self.encode_with_intermediate(style)
        content_feat = self.encode(content)
        style_feat = self.encode(style)
        # decoder
        t = content_feat
        content_t = self.graph(style_feat, content_feat)
        content_t = adaptive_instance_normalization(content_t, style_feat)

        content_t = self.dec_2(content_t)
        output = self.dec_3(content_t)
        g_t_feats = self.encode_with_intermediate(output)

        # loss
        loss_c = self.calc_content_loss(g_t_feats[-1], t)
        loss_s = 0.
        for i in range(3):
            loss_s += self.calc_style_loss(g_t_feats[i], style_feats[i])
        if is_training == True:
            return g_t_feats[-1],loss_c,loss_s
        return output, loss_c, loss_s
        
